#Create tuple
print("Create tuple")
t=tuple((1,2,3,4,5,6,5,7,5))
print(t)

#count the values in tuple
print("count the values in tuple")
b=t.count(5)
print(b)

#Indexing of tuple
c=t.index(2)
print(c)

#Delete of tuple
a=("teena","meena","reena")
del a
print(a)

